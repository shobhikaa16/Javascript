//Switch case statement in javascript
const month = 3;
//const ="jan" // so use jan in place of 1,2,3

switch(month){
    case 1:
        console.log("January");
        break;
    case 2:
        console.log("Feb");
        break;
    case 3:
            console.log("March");
            break; 
    default:
        console.log("Invalid");               
}